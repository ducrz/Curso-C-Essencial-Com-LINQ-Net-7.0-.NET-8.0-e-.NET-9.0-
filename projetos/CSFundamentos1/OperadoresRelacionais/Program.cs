/* Operadores Relacionais em C#

A características dos operadores relacionais em C# indicam que o resultado
de uma operação relacional é sempre um valor booleano (true ou false).
== -> igualdade
!= -> diferente
>  -> maior que
<  -> menor que
>= -> maior ou igual a
<= -> menor ou igual a 
Exemplo: int x=10 e int y=20
x == y -> false
x != y -> true
x > y  -> false
x < y  -> true
x >= y -> false
x <= y -> true  
*/

